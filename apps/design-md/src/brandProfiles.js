export function getBrandProfile(brand) {
  if (!brand) return null;
  const direct = brandProfiles[brand.id];
  if (direct) return direct;

  if (brand.kicker && brand.kicker !== '批量提炼版') {
    return { category: '品牌简介', intro: brand.kicker };
  }

  if (brand.summary) {
    const intro = String(brand.summary).split(/[。.!?]/)[0].trim();
    if (intro) return { category: '品牌简介', intro };
  }

  return null;
}
